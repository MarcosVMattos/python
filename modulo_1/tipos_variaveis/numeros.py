# Inteiro
numero_inteiro = 3
print("Inteiro =", numero_inteiro)

# Real com ponto flutuante
numero_real = 3.14
print("Real com ponto flutuante =", numero_real)

# type()
print("Tipo da variável inteiro =", type(numero_inteiro))
print("Tipo da variável real =", type(numero_real))

# Soma +
soma = 1 + 1
print("1 + 1 =", soma)

# Subtração 
subtracao = 1 - 1
print("1 - 1 =", subtracao)

# Multiplicação *
multiplicacao = 2 * 2
print("2 x 2 =", multiplicacao)

# Divisão /
divisao = 5 / 2
print("5 / 2 =", divisao, type(divisao))

# int()
print("Transformando o resultado da divisão em número inteiro:", int(divisao))

# float()
print("Transformando o resultado da soma em número real:", float(soma))

# Módulo %
modulo = 5 % 2
print("Módulo =", modulo)

# Divisão inteiro //
divisao_int = 5 // 2
print("5 / 2 =", divisao_int)
print("Tipo da variável do resultado da divisão inteira =", type(divisao_int))