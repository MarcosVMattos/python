# Criando uma tupla de exemplo
minha_tupla = (1, 7, 2, 2, 3, 4, 1, 2)
print("Minha tupla:", minha_tupla)

print("minha_tupla[0]:", minha_tupla[0])
print("minha_tupla[2]:", minha_tupla[2])
print("minha_tupla[-1]:", minha_tupla[-1])

# Método count: conta quantas vezes aparece determinado número
print("Quantidade de vezes que o elemento 2 aparece:", minha_tupla.count(2))

# Index: mostra o índice de determinado elemento (da primeira ocorrência)
print("Índice da primeira ocorrência do elemento 7:", minha_tupla.index(7))